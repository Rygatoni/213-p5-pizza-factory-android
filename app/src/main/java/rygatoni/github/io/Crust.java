package rygatoni.github.io;

/**
 * The Crust enum stores the types of pizza crust that RU Pizzeria sells
 *
 * @author Rygl Ato
 * @author Jeffrey Mijares
 */
public enum Crust {
    /**
     * Deep Dish type of crust
     */
    DEEP_DISH,
    /**
     * Pan type of crust
     */
    PAN,
    /**
     * Stuffed type of crust
     */
    STUFFED,
    /**
     * Brooklyn type of crust
     */
    BROOKLYN,
    /**
     * Thin type of crust
     */
    THIN,
    /**
     * Hand Tossed type of crust
     */
    HAND_TOSSED
}
